    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String id=jTextField1.getText();
        char ch[]=jPasswordField1.getPassword();
        String pass=new String(ch);
        Criteria crit=session.createCriteria(UserSignUp.class);
        crit.add(Restrictions.and(Restrictions.eq("userId",id),Restrictions.eq("password", pass)));
        List<UserSignUp> list=crit.list();
        if(list.isEmpty())
            jLabel7.setText("Invalid Id/Password");
        else
        {
            new UserPage().setVisible(true);
            dispose();
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

